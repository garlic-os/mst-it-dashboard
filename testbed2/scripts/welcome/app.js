function setMode(mode){
    switch(mode){
        case 1:
            window.location.replace("./callcenter.html");
          break;
        case 2:
            window.location.replace("./walkin.html");
          break;
        case 3:
            window.location.replace("./pros.html");
          break;
        case 4:
            window.location.replace("./deployment.html");
          break;
    }        
}